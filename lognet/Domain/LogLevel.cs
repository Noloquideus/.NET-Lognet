namespace lognet.Domain;


///  Enum representing different log levels.
public enum LogLevel
{
    // Debug log level (numeric value: 100).
    Debug = 100,

    // Information log level (numeric value: 200).
    Info = 200,

    // Warning log level (numeric value: 300).
    Warning = 300,

    // Error log level (numeric value: 400).
    Error = 400,

    // Critical log level (numeric value: 500).
    Critical = 500
}
